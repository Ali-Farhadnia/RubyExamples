# Data Types Example

# Different data types in Ruby
string_var = "Hello, Ruby!"
integer_var = 42
float_var = 3.14
boolean_var = true
array_var = [1, 2, 3, 4]
hash_var = { "name" => "Alice", "age" => 25 }

# Outputting variables and their types
puts "String variable: #{string_var} (Type: #{string_var.class})"
puts "Integer variable: #{integer_var} (Type: #{integer_var.class})"
puts "Float variable: #{float_var} (Type: #{float_var.class})"
puts "Boolean variable: #{boolean_var} (Type: #{boolean_var.class})"
puts "Array variable: #{array_var} (Type: #{array_var.class})"
puts "Hash variable: #{hash_var} (Type: #{hash_var.class})"
