# Conditional Statements Example

# if-else statement
grade = 85

if grade >= 90
  puts "Grade: A"
elsif grade >= 80
  puts "Grade: B"
elsif grade >= 70
  puts "Grade: C"
else
  puts "Grade: F"
end

# Output: "Grade: B"

# Switch Case Simulation using case statement
fruit = "apple"

case fruit
when "apple"
  puts "It's an apple!"
when "banana"
  puts "It's a banana!"
when "orange"
  puts "It's an orange!"
else
  puts "It's something else!"
end

# Output: "It's an apple!"
