# Loops Example

# while loop
puts "while loop:"
count = 1

while count <= 5
  puts "Count: #{count}"
  count += 1
end
=begin
Output:"
while loop:
Count: 1
Count: 2
Count: 3
Count: 4
Count: 5
"
=end


# for loop
puts "for loop:"
for i in 1..5
  puts "Value: #{i}"
end

=begin
Output:"
for loop:
Value: 1
Value: 2
Value: 3
Value: 4
Value: 5
"
=end
